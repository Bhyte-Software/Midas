package com.bhyte.midas.Recycler;

public class QuickActionsHelperClass {

    int image;
    String title;

    public QuickActionsHelperClass(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

}
